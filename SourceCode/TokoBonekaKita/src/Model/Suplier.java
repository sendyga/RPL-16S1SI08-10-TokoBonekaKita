public class Suplier {
    private String kd_sup;
    private String alamat;
    private int no_tlp;
    private String nama_sup;

    public String getKd_sup() {
        return kd_sup;
    }

    public void setKd_sup(String kd_sup) {
        this.kd_sup = kd_sup;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public int getNo_tlp() {
        return no_tlp;
    }

    public void setNo_tlp(int no_tlp) {
        this.no_tlp = no_tlp;
    }

    public String getNama_sup() {
        return nama_sup;
    }

    public void setNama_sup(String nama_sup) {
        this.nama_sup = nama_sup;
    }
}
